def print_board(board):
    columns = [f'  {i} ' for i in 'ABCDEFGHI']
    print('  ' + ''.join(columns))
    print('  +===' + '+==='*8 + '+')
    for i in range(len(board)):
        print(str(i) + ' | ' + " : ".join(board[i][0:3]) + 
            ' | ' + " : ".join(board[i][3:6]) + 
            ' | ' + " : ".join(board[i][6:9]) + ' |')
        if i != len(board)-1 and i % 3 != 2 :
            print('  +---' +'+---'*8+'+')
        elif i % 3 != 2 :
            print('  +===' +'+==='*8+'+')
        else:
            print('  +===' +'+==='*8+'+')
    print()
